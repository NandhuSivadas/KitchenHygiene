    c.drawString(260, y_pos, issue_date.strftime('%B %d, %Y'))
    
    y_pos -= 25
    c.setFont("Helvetica", 11)
    c.drawString(120, y_pos, f"Valid Until:")
    c.setFont("Helvetica-Bold", 11)
    c.drawString(260, y_pos, valid_till.strftime('%B %d, %Y'))
    
    y_pos -= 25
    c.setFont("Helvetica", 11)
    c.drawString(120, y_pos, f"Hotel Address:")
    c.setFont("Helvetica-Bold", 11)
    c.drawString(260, y_pos, hotel.hotel_address[:40])
    
    # Signature section
    y_pos = 180
    c.setFont("Helvetica", 10)
    c.setFillColorRGB(0, 0, 0)
    
    # Left signature
    c.line(100, y_pos, 250, y_pos)
    c.drawCentredString(175, y_pos - 20, "Authorized Signature")
    c.setFont("Helvetica-Oblique", 9)
    c.drawCentredString(175, y_pos - 35, "RateMyKitchen AI System")
    
    # Right signature
    c.setFont("Helvetica", 10)
    c.line(width - 250, y_pos, width - 100, y_pos)
    c.drawCentredString(width - 175, y_pos - 20, "Date of Issue")
    c.setFont("Helvetica-Bold", 9)
    c.drawCentredString(width - 175, y_pos - 35, issue_date.strftime('%B %d, %Y'))
    
    # Seal/Badge
    c.setFillColorRGB(0.79, 0.66, 0.38)
    c.setStrokeColorRGB(0.79, 0.66, 0.38)
    c.setLineWidth(2)
    c.circle(width - 100, height - 120, 40, fill=False, stroke=True)
    c.setFont("Helvetica-Bold", 10)
    c.drawCentredString(width - 100, height - 115, "CERTIFIED")
    c.setFont("Helvetica", 8)
    c.drawCentredString(width - 100, height - 130, "CLEAN")
    
    # Footer
    c.setFillColorRGB(0.5, 0.5, 0.5)
    c.setFont("Helvetica-Oblique", 8)
    c.drawCentredString(width / 2, 60, "This certificate is valid for 90 days from the date of issue")
    c.drawCentredString(width / 2, 45, "RateMyKitchen - AI-Powered Kitchen Hygiene Intelligence")
    
    c.save()
    return output_path
