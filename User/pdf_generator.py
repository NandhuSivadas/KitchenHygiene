"""
PDF Generation Utilities for RateMyKitchen
Generates violation reports and certificates
"""

from reportlab.lib.pagesizes import letter, A4
from reportlab.lib.units import inch
from reportlab.pdfgen import canvas
from reportlab.lib import colors
from reportlab.platypus import Table, TableStyle
from datetime import datetime, timedelta
import os


def generate_violation_report(hotel, upload_model, violations_list, output_path):
    """
    Generate PDF report for dirty or moderately clean kitchens
    
    Args:
        hotel: tbl_hotel instance
        upload_model: UploadModel instance with hygiene_status
        violations_list: List of detected violations
        output_path: Path to save the PDF
    
    Returns:
        Path to generated PDF
    """
    c = canvas.Canvas(output_path, pagesize=letter)
    width, height = letter
    
    # Header
    c.setFillColorRGB(0.79, 0.66, 0.38)  # Gold color
    c.rect(0, height - 100, width, 100, fill=True, stroke=False)
    
    c.setFillColorRGB(1, 1, 1)  # White text
    c.setFont("Helvetica-Bold", 24)
    c.drawCentredString(width / 2, height - 50, "HYGIENE VIOLATION REPORT")
    
    c.setFont("Helvetica", 12)
    c.drawCentredString(width / 2, height - 75, "RateMyKitchen AI Analysis")
    
    # Hotel Information
    y_position = height - 140
    c.setFillColorRGB(0, 0, 0)
    c.setFont("Helvetica-Bold", 14)
    c.drawString(50, y_position, "Hotel Information")
    
    y_position -= 25
    c.setFont("Helvetica", 11)
    c.drawString(70, y_position, f"Hotel Name: {hotel.hotel_name}")
    y_position -= 20
    c.drawString(70, y_position, f"Address: {hotel.hotel_address}")
    y_position -= 20
    c.drawString(70, y_position, f"Contact: {hotel.hotel_contact}")
    
    # Analysis Details
    y_position -= 40
    c.setFont("Helvetica-Bold", 14)
    c.drawString(50, y_position, "Analysis Details")
    
    y_position -= 25
    c.setFont("Helvetica", 11)
    c.drawString(70, y_position, f"Scan Date: {upload_model.uploaded_at.strftime('%B %d, %Y at %I:%M %p')}")
    y_position -= 20
    
    # Hygiene Status with color
    status = upload_model.hygiene_status
    if status == "Dirty":
        c.setFillColorRGB(0.93, 0.27, 0.27)  # Red
    else:  # Moderately Clean
        c.setFillColorRGB(0.96, 0.62, 0.04)  # Orange
    
    c.setFont("Helvetica-Bold", 12)
    c.drawString(70, y_position, f"Hygiene Status: {status}")
    
    # Violations Section
    y_position -= 40
    c.setFillColorRGB(0, 0, 0)
    c.setFont("Helvetica-Bold", 14)
    c.drawString(50, y_position, "Violations Detected")
    
    y_position -= 25
    c.setFont("Helvetica", 11)
    
    if violations_list:
        for i, violation in enumerate(violations_list, 1):
            if y_position < 100:  # New page if needed
                c.showPage()
                y_position = height - 50
            
            c.setFillColorRGB(0.93, 0.27, 0.27)
            c.circle(80, y_position + 3, 3, fill=True)
            c.setFillColorRGB(0, 0, 0)
            c.drawString(95, y_position, f"{violation.capitalize()}")
            y_position -= 20
    else:
        c.drawString(70, y_position, "No specific violations detected")
        y_position -= 20
    
    # Recommendations
    y_position -= 30
    if y_position < 200:
        c.showPage()
        y_position = height - 50
    
    c.setFont("Helvetica-Bold", 14)
    c.drawString(50, y_position, "Recommendations")
    
    y_position -= 25
    c.setFont("Helvetica", 10)
    recommendations = [
        "• Immediate deep cleaning of all kitchen surfaces required",
        "• Address all detected violations before resuming operations",
        "• Implement strict hygiene protocols and staff training",
        "• Schedule follow-up inspection within 7 days",
        "• Maintain daily cleaning logs and documentation"
    ]
    
    for rec in recommendations:
        if y_position < 100:
            c.showPage()
            y_position = height - 50
        c.drawString(70, y_position, rec)
        y_position -= 18
    
    # Footer
    c.setFillColorRGB(0.5, 0.5, 0.5)
    c.setFont("Helvetica-Oblique", 9)
    c.drawCentredString(width / 2, 50, "This report was generated by RateMyKitchen AI Analysis System")
    c.drawCentredString(width / 2, 35, f"Generated on: {datetime.now().strftime('%B %d, %Y at %I:%M %p')}")
    
    c.save()
    return output_path


def generate_certificate(hotel, upload_model, output_path):
    """
    Generate certificate PDF for clean kitchens
    
    Args:
        hotel: tbl_hotel instance
        upload_model: UploadModel instance
        output_path: Path to save the PDF
    
    Returns:
        Path to generated PDF
    """
