    width, height = letter
    
    # Decorative border
    c.setStrokeColorRGB(0.79, 0.66, 0.38)  # Gold
    c.setLineWidth(3)
    c.rect(30, 30, width - 60, height - 60, fill=False, stroke=True)
    
    c.setLineWidth(1)
    c.rect(40, 40, width - 80, height - 80, fill=False, stroke=True)
    
    # Header
    c.setFillColorRGB(0.79, 0.66, 0.38)
    c.setFont("Helvetica-Bold", 32)
    c.drawCentredString(width / 2, height - 120, "CERTIFICATE")
    
    c.setFont("Helvetica", 16)
    c.drawCentredString(width / 2, height - 150, "OF KITCHEN HYGIENE EXCELLENCE")
    
    # Decorative line
    c.setStrokeColorRGB(0.79, 0.66, 0.38)
    c.setLineWidth(2)
    c.line(150, height - 170, width - 150, height - 170)
    
    # Certificate body
    c.setFillColorRGB(0, 0, 0)
    c.setFont("Helvetica", 14)
    c.drawCentredString(width / 2, height - 220, "This is to certify that")
    
    # Hotel name
    c.setFont("Helvetica-Bold", 22)
    c.setFillColorRGB(0.79, 0.66, 0.38)
    c.drawCentredString(width / 2, height - 260, hotel.hotel_name.upper())
    
    # Certificate text
    c.setFillColorRGB(0, 0, 0)
    c.setFont("Helvetica", 13)
    c.drawCentredString(width / 2, height - 300, "has successfully passed the AI-powered kitchen hygiene inspection")
    c.drawCentredString(width / 2, height - 325, "and has been awarded this certificate for maintaining")
    
    c.setFont("Helvetica-Bold", 15)
    c.setFillColorRGB(0.13, 0.72, 0.51)  # Green
    c.drawCentredString(width / 2, height - 355, "EXCELLENT HYGIENE STANDARDS")
    
    # Details
    c.setFillColorRGB(0, 0, 0)
    c.setFont("Helvetica", 11)
    
    issue_date = datetime.now()
    valid_till = issue_date + timedelta(days=90)  # Valid for 90 days
    
    y_pos = height - 420
    c.drawString(120, y_pos, f"Certificate Number:")
    c.setFont("Helvetica-Bold", 11)
